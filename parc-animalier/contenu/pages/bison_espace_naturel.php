		<h4>Description</h4>
		<p>Sa silhouette, gravée ou peinte, figure dans les grottes ornées de Pyrénées des des ossements découverts dans les nombreux sites attestent de la présence du bison.</p>
		<p>En France, le bison a disparu au 9° siècle. Les derniers individus sauvages vivaient en Pologne et dans le Caucase et l'espèce était en voie de disparition en début du 20° siècle. Le programme international, conduit depuis 1929, a permis de la sauvegarder à partir de quelques éléments.</p>
		<p>Le bison, le plus gros des mammifères européens, est un animal des forêts et des terrains marécageux. Il est élancé et rapide, et vit en groupes très hiérarchisés. Les vaches s'en écartent au moment des mises bas.</p>
		<p>La période du rut peut donner lieu à de terribles combats opposants les mâles pour la possession des femelles.</p>
