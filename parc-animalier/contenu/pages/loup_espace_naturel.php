		<h4>Description</h4>
		<p>Ce mammifère carnivore de la famille des canidés a jadis habité toute la chaîne pyrénéenne. Il existe aujourd'hui une population en légère expansion dans les monts Cantabriques qui constituent le prolongement occidental de la chaîne des Pyrénées.</p>
		<p>Le loup a fait l'objet, aux XVIII° et XIX° siècles, de campagnes d'extermination (battues, empoisonnement), qui ont précipités son déclin. Les derniers loups observés dans les Pyrénées Orientales l'ont été vers 1923. L'espèce s'est maintenue plus longtemps à l'ouest de la chaîne (1935). Les anciens distinguaient une deuxième forme, mais cette scission reposait sur l'observation d'individus sombres.</p>
		<p>La biologie pyrénéenne du loup est mal connue. Il semble que l'espèce ait préféré les zones alternants bois et cultures du Pièmont aux forêts montagnardes où elle était moins fréquente.</p>
		<p>Le loup est un animal social, qui développe un riche tissu de relations hiérarchiques entre individus. La meute (ou clan) est sous la dominance d'un mâle adulte, associé à une femelle, sa partenaire, qui est également dominante.</p>				
