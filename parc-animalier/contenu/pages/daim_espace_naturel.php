		<h4>Description</h4>
		<p>Des restes attribués à des daims préhistoriques ont été découverts dans la Caume de l'Arago à Tautavel. Quelques daims échappés d'élevage vivent à l'état sauvage dans les Pyrénées.</p>
		<p>Comme tous les cervidés, le daim perd ses bois chaque année en fin d'hiver : ils repoussent au printemps sous une peau protectrice, le velours. L'extrémité des bois se nomme la "palmure" en raison de sa forme palmée.</p>
		<p>Le daim est vénéré par les habitants de l'île de Rhodes : les serpents envahissent l'île. Sur les conseil de l'oracle de Delphes, les daims furent lâchés et en quelques années, ils débarrassèrent l'île de ses serpents en les piétinant. De nombreuses observations ont permis de vérifier le comportement agressif des daims vis-à-vis des reptiles. Ce cervidé originaire d'Asie a été introduit en Gaule par les Romains.</p>
