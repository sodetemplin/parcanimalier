		<h4>Description</h4>
		<p>C'est l'animal symbole des Pyrénées. Plus gracile que son cousin alpin le chamois, l'isard a une morphologie bien adaptée à la haute montagne.</p>
		<p>Son pelage sombre lui permet de capter les rayons du soleil, mais surtout, il a un cœur qui pèse 350 grammes, et un sang riches en globules rouges : il ne faut pas compter sur lui pour s'essouffler en détalant !</p>
		<p>On ne peut être qu'émerveillé par son agilité sur les rocs et les névés.</p>
		<p>La particularités de ses sabots atteste de son caractère montagnard : la partie extérieure est dure et aiguisée, sorte de couteau, elle lui permet ainsi de progresser facilement sur la glace ou la neige dure. Une petite éponge, placée dans les sens longitudinal du sabot fait office d'antidérapant.</p>
		<p>Craignant plus la faim que le froid, on peut le rencontrer l'hiver sur les hautes crêtes et cols où le vent déneige le tapis végétal, ainsi d'ailleurs que dans les grandes forêts de moindres altitudes (1000m dans les Pyrénées audoises).</p>
