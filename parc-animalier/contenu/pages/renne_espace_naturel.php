		<h4>Description</h4>
		<p>Le renne est largement représenté sur les parois des grottes préhistoriques des Pyrénées. Il a pourtant quitté nos contrées il y a 12000 ans, repoussé vers le nord par le réchauffement du climat. C'est le seul cervidé dont la femelle porte des bois, comme le mâle.</p>
		<p>Les femelles et les mâles perdent leurs bois à la fin du printemps. La présence ou non de ramures, ainsi que la taille des animaux, jouent un grand rôle dans l'organisation sociale des hardes.</p>
		<p>Le pelage d'hiver est très épais et isolant. Les sabots très larges et anti-dérapants sont adaptés à la marche dans la neige et dans les tourbières. Lors de déplacements, les onglons de chaque pied se heurtent, émettant un cliquet caractéristique. Les rennes sont très friands des lichens de conifères et les préfèrent à toute autre alimentation.</p>
