		<h4>Description</h4>
		<p>Le bouquetin pyrénéen figurait déjà comme d'autres animaux du Parc sur les gravures et peintures préhistoriques (- 18000 ans), particulièrement à Niaux (Ariège).</p>
		<p>Tant qu'il n'y eut pas d'armes à feu et que les techniques de chasses restèrent rudimentaires, le bouquetin habitait l'ensemble des Pyrénées. Il disparut du versant français au début du 19° siècle. Animal robuste, très habile grimpeur, il défiait les prédateurs du haut des parois inaccessibles où ses exceptionnels qualités d'équilibre, croyait il, le rendait invulnérable. Hélas, c'était sans compter avec les hommes et leurs fusils !</p>
		<p>Le bouquetin pyrénéen subsiste (une douzaine d'animaux) dans les vallées d'Ordessa, sur le versant espagnol. Ailleurs, en Espagne, de fortes colonnies de bouquetins ibériques, très proches parents de celui des Pyrénées (morphologie adaptée au pays, cornes différentes par exemple) occupent un massif au sud de Madrid et près de la côte est, à l'embouchure de l'Ebre.</p>
		<p>Les mesures de protection prises et les études communes en cours, entre scientifiques espagnols et français, laissent penser que la réintroduction sur l'ensemble de la chaîne se fera dans un proche avenir.</p>
		<p>Le mâle et la femelle diffèrent par le poids et les cornes.</p>
