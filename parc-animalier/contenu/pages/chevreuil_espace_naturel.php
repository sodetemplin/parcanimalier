		<h4>Description</h4>
		<p>Devenu très rare au 16° siècle, le chevreuil a re-colonisé les Pyrénées grâce à des lâchers massifs survenus dans les années 1960/1970.</p> 
		<p>C'est le plus petit et le plus discret des cervidés européens dont il diffère par la perte des bois en automne au lieu du printemps. La croissance de ceux-ci se fait en hiver et les bois sont nettoyés  de leurs velours en février/mars. Peu sociable et farouchement territorial, le mâle défend le secteur où il a choisi de vivre. Il n'accepte aucune présence sur son domaine.</p>
		<p>L'époque des amours prend place en plein été et non en automne. La gestation est de 5 mois et demi, mais afin que les faons ne naissent pas en plein hiver, la chevrette, saillie en juillet/août, diffère jusqu'en automne l'implantation de l'embryon. Les faons naissent en mai.</p>
