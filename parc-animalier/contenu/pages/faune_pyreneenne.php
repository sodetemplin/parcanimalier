		<h4>Description</h4>
		<p>L'ours des Pyrénées appartient à l'espèce Ursus Arctos, largement répandue dans l'Ancien et le Nouveau Monde. Si le plantigrade est aujourd'hui réduit à un effectif d'une dizaine de sujets, il fut jadis répandu sur toute la chaîne des Pyrénées. Comme le loup, l'ours fit l'objet de battues, ainsi que de campagnes d'empoisonnements par l'homme qui lui reprochait de s'attaquer aux troupeaux.</p>
		<p>L'ours, à la fois craint et admiré est une composante majeures des mythes et légendes des montagnes. Dans les Pyrénées, où il est assimilé symboliquement à un "sous-homme", il est le héros de fêtes ou carnavals de l'Ours.</p>
		<p>Aujourd'hui repoussé dans les forêts les plus difficiles d'accès, l'ours est essentiellement béarnais mais l'espèce survit à l'état relictuel dans les montagnes pyrénéennes.</p>
