<p>Le parc animalier des Angles est entièrement situé dans une <strong>forêt de pins à crochets</strong>. Ce résineux tire son nom des crochets bien visibles sur ses cônes.
La pinède de pins à crochets se rencontre, en général, de 1500m à 2400m d'altitude en Cerdagne-Capcir. Ce pin pousse au soleil ou à l'ombre, sur un terrain sec ou humide.
Le sous-bois est colonisé par des plantes herbacées, des arbustes ou d'autres essences d'arbres qui s’accommodent de l'ombre et de l'acidité du sol.</p>
<p>Dix plantes représentatives de ces forêts sont présentées ci-dessous, et visibles au parc sur notre <strong>sentier botanique</strong> (selon la période de l'année). <strong>Ne pas les cueillir ! Et attention, certaines sont toxiques !</strong></p>

