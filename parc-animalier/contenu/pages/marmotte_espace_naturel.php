		<h4>Description</h4>
		<p>La marmotte, disparue des Pyrénées à l'époque préhistorique (restes osseux sur les sites d'occupation humaine), a été réintroduite dans le massif entre 1948 et 1960. Elle a très vite colonisé la montagne entre 1400 et 2600m d'altitude.</p>
		<p>Animal diurne, elle a la particularité d'hiberner durant 6 mois (novembre à avril). Les mécanismes de l'hibernation sont complexes.</p>
		<p>La marmotte vit en colonies de 3 à 30 individus, dans un ensemble de terriers bien exposés. La journée d'une marmotte, de l'aube au crépuscule, se fractionne entre les repas, les siestes, les jeux, le guet et l'alerte ...</p>
		<p>La communication se fait principalement par coups de sifflets stridents et autres signaux sonores et visuels. On peut observer l'animal dans la position normal, à quatre pattes ou dans la position assise, dite en "chandelier", utilisée pour le guet en particulier.</p>
