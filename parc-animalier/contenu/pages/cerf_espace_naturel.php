		<h4>Description</h4>
		<p>Les peintures rupestres des grottes pyrénéennes (et localement sur des rocs gravés) présentent souvent l'image du cerf. Réintroduits en 1950, les cerfs des Pyrénées sont considérés comme les plus grands et les plus beaux de France.</p>
		<p>Le cerf élaphe perd ses bois chaque année en fin d'hiver : ils repoussent plus forts et plus ramifiés en 3 mois. Un cartilage se développe sous une peau protectrice appelée "velours". Une fois la pousse terminée, le cerf frotte ses bois contre la végétation pour se débarrasser des velours : il fraye.</p>
		<p>Le brame, époque des amours, prend place de la mi-septembre à la mi-octobre. La montagne résonne alors des "raires" puissants des mâles. Durant cette période, le cerf peut perdre jusqu'à 1/3 de son poids.</p>
		<p>Huit mois après naissent les faons. Les biches viennent les voir régulièrement pour les allaiter et les toiletter, ne restant jamais auprès d'eux pour ne pas attirer les prédateurs. Aussi, ne touchez jamais un faon seul en forêt. Il n'est pas abandonné, la biche n'est pas loin et veille !</p>
