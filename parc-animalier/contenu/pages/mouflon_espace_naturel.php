		<h4>Description</h4>
		<p>Bien qu'un grand mouflon ait vécu en France il y a plus de 300000 ans (on peut voir des fossiles au musée de Tautavel), les mouflons sont peu représentés dans l'art préhistorique.</p>
		<p>Quant au mouflon de Corse, il n'a jamais existé naturellement en France et a été introduit à partir des populations naturelles vivant en Corse dans les années 1960 (Fenouillèdes et Massif du Carlit).</p>
		<p>Bien que l'on puisse rencontrer en été le mouflon à des altitudes dépassant les 2000m, sa morphologie et son comportement, malhabile sur la neige, et moins agile sur les escarpements, soulignent des origines non montagnardes.</p>
