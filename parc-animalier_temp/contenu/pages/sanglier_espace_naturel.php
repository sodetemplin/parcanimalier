		<h4>Description</h4>
		<p>Les sangliers présents dans le parc animalier sont purs, ce qui est assez rares ailleurs. En effet, certains éleveurs ont croisé l'espèce avec le porc domestique et des individus croisés ont ainsi pollué génétiquement les populations sauvages.</p>
		<p>Le sanglier est actif, surtout de nuit : il aime l'eau et se vautre dans la boue.</p>
		<p>Lors de la mise bas, la femelle (la laie), s'isole dans un endroit couvert et bien aéré, proche d'un site où la nourriture est abondante. Elle établit alors un chaudron, garni de branchages, feuilles et herbes sèches.</p>
		<p>Elle ne quittera ce lieu que huit jours après la naissance des marcassins, pendant lesquels elle ne s'alimente pas.</p>
		<p>Par réflexe de sécurité, les mères et jeunes se regroupent en bande jusqu'aux prochaines naissances. Le mâle vit isolé.</p>
		<p>Les défenses, attributs du mâle, caractérisent la force et la domination. Elles sont usées et aiguisées par frottement sur les grés (placés sur la mâchoire supérieure).</p>
